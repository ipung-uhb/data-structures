# importing array module
import array as arr
  
# array dengan tipe integer
a = arr.array('i', [1, 2, 3, 4, 5, 6])
  
# mengakses elemen sebuah array
print("Akses element index ke 0: ", a[0])
  
# accessing element of array
print("Akses element index ke 3: ", a[3])
  
# array dengan tipe float
b = arr.array('d', [2.5, 3.2, 3.3])
  
# mengakses elemen sebuah array
print("Akses element index ke 1: ", b[1])
  
# mengakses elemen sebuah array
print("Akses element index ke 2: ", b[2])